<script type="text/javascript">

    $("#cs_violation").addClass("active"); 

    var violation_table; 
    var link1 = '<?php echo $this->security->get_csrf_token_name();?>';
    var link2 = '<?php echo $this->security->get_csrf_hash();?>';
    var equals = '=';
    var url = '<?php echo site_url('main_controller/violation_table_cons');?>';

    $("#deduction").addClass("active");      
    violation_table =  $('#violation_table').DataTable(
    {
    // "ordering": false,
    "order": [[2, "asc" ]],
    "ajax": {
    "url": url,
    "type": "POST",
    "data":function(data) {
    data.vio_cutoff_date = $('#vio_cutoff_date').val();                                      
    data.link1+ equals +link2;        
    },
    },
      "columnDefs": [
          { className: "text-right", "targets": [3] } ,
          { className: "text-center", "targets": [4,5] },
      ],           
    }).page.len(100).draw();


    var select_all = document.getElementById("select_all"); //select all checkbox
    var checkboxes = document.getElementsByClassName("check_box"); //checkbox items

    $('#select_all').click(function () 
    {
        violation_table.page.len(1000).draw();
        select_all.addEventListener("change", function(e)
        {
            for (i = 0; i < checkboxes.length; i++) 
            { 
                checkboxes[i].checked = select_all.checked;
            }   
        });
    });

    $("#submit_violation_btn").click(function(event){

    if ($("#check_box:checked").length == 0)
    {
        $('#no_checkbox_selected').modal('show');
    }
    else
    {
      $('#submit_violation_modal').modal('show'); 
      
      $("#submit_violation_modal_btn").unbind('click').bind('click', function()       
      { 
        $('#submit_violation_modal_btn').prop('disabled', true);
        var check = $("input[class='check_box']:checked").map(function() {
        return this.value;
        }).get();

        $.ajax(
        {
            url:'<?php echo site_url('main_controller/submit_violation_con');?>',
            type:'POST',
            data: {check:check},
            success:function(data)
            {   
                // $('#submit_deduction_modal').modal('hide');
                // $('#submit_deduction_con_btn').prop('disabled', false);
                $('#select_all').attr('checked', false);

                $("#vio_cutoff_date option:selected").remove();
                $('#submit_violation_modal').modal('hide'); 
                $('#success_modal').modal('show');
                $('#submit_violation_modal_btn').prop('disabled', false);
                violation_table.ajax.reload(null, false);  
           
            }
        });
      });        
    }
});

$("#vio_cutoff_date").change(function()
{
    var vio_cutoff_date = document.getElementById("vio_cutoff_date").value;

    if(vio_cutoff_date == 'Please select date')
    {
        $('#deduction_date_modal').modal('show');
    }
    else
    {  
        violation_table.ajax.reload(null, false);                   

    }
});


